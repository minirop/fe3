#!/usr/bin/env python

import os, fe

addresses = [0x89C4DA, 0x89C4E9, 0x89C4F8, 0x89C507, 0x89C520, 0x89C535, 0x89C54E, 0x89C56D, 0x89C58A, 0x89C5A5, 0x89C5C4, 0x89C5E3, 0x89C5FA, 0x89C615, 0x89C634, 0x89C64F, 0x89C66C, 0x89C685, 0x89C6A4, 0x89C6C3, 0x89C6DC, 0x89C6FB, 0x89C70C, 0x89C72B, 0x89C74A, 0x89C75D, 0x89C776, 0x89C795, 0x89C7AE, 0x89C7CD, 0x89C7EC, 0x89C80B, 0x89C824, 0x89C843, 0x89C85C, 0x89C87B, 0x89C89A, 0x89C8B7, 0x89C8D0, 0x89C8EF, 0x89C90E, 0x89C92D, 0x89C946, 0x89C965, 0x89C984, 0x89C9A3]

rom = fe.rom()

for i in range(0, len(addresses) - 1):
	start = addresses[i] - 0x850000
	end = addresses[i+1] - 0x850000

	blob = rom[start:end]
	print ("\n\nL{:X}:".format(addresses[i]), end = '')
	for (i, b) in enumerate(blob):
		if i % 8 == 0:
			print ("\n.db", end = '')

		print (" ${:02X}".format(b), end = '')
print ("")
