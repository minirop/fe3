#!/usr/bin/env python

import os, fe

addresses = [0xD08000, 0xD08181, 0xD08615, 0xD0877C, 0xD088E4, 0xD08A26, 0xD090FE, 0xD09300, 0xD09641, 0xD09849, 0xD09AE7, 0xD09C34, 0xD09FD2, 0xD0A227, 0xD0A935, 0xD0ABF4, 0xD0AF35, 0xD0B0F5, 0xD0B2D2, 0xD0B4AF, 0xD0B5B2, 0xD0B791, 0xD0B8D1, 0xD0BC01, 0xD0BE5D, 0xD0C016, 0xD0C144, 0xD0C3C8, 0xD0C477, 0xD0C538, 0xD0C80C, 0xD0CA38, 0xD0CB87, 0xD0CC44, 0xD0CDCD, 0xD0CE42, 0xD0D054, 0xD0D0E7, 0xD0D220, 0xD0D2B6, 0xD0D592, 0xD0D88F, 0xD0DA1A, 0xD0DABC, 0xD0DC1A, 0xD0DD5E, 0xD0DE95, 0xD0FFFF]

rom = fe.rom()

for i in range(0, len(addresses) - 1):
	start = fe.cpu_to_prg(addresses[i])
	end = fe.cpu_to_prg(addresses[i+1])

	blob = rom[start:end]
	print ("\n\nL{:X}:".format(addresses[i]), end = '')
	for (i, b) in enumerate(blob):
		if i % 8 == 0:
			print ("\n.db", end = '')

		print (" ${:02X}".format(b), end = '')
print ("")
